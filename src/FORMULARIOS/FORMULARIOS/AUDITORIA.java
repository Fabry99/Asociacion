        else buscador = "TODOS";
        String bus2 = "", bTipo = "", bFPago = "";
        bus2 = txtcliente.getText().toUpperCase();
        bus = "";
        bus = txtDui.getText().toUpperCase();
        bTipo = cbTipoCuenta.getSelectedItem().toString().toUpperCase();
        bFPago = cbFormapago.getSelectedItem().toString().toUpperCase();
